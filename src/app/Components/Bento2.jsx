const fetchBlogByPage = async () => {
  const resp = await fetch(
    `https://www.kshinfra.com/wp-json/wp/v2/posts?per_page=6&page=1`,
    { next: { revalidate: 60 } }
  );

  if (!resp.ok) {
    throw new Error("Failed to fetch blog data");
  }

  const data = await resp.json();
  return data;
};

